//! Алгоритм автоматического просчёта таймингов групп.
//!
//! Все группы используют ЕДИНЫЙ цикл (120с) с фазовым сдвигом 10с.
//! Это гарантирует отсутствие коллизий при любом количестве групп.
//!
//! Пример для 6 групп (2 хеша × 3 группы/хеш):
//!   Группа 1: старт t=0,   ротации: 120, 240, 360, ...
//!   Группа 2: старт t=10,  ротации: 130, 250, 370, ...
//!   Группа 3: старт t=20,  ротации: 140, 260, 380, ...
//!   Группа 4: старт t=30,  ротации: 150, 270, 390, ...
//!   Группа 5: старт t=40,  ротации: 160, 280, 400, ...
//!   Группа 6: старт t=50,  ротации: 170, 290, 410, ...
//!
//! Зазор между любыми двумя ротациями ровно 10с.
//! Коллизии невозможны математически.

const CYCLE_SECS: u64 = 600; // 10 минут — креды живут 15-20 мин, ротация с запасом
const PHASE_STEP_SECS: u64 = 10;

/// Возвращает (initial_delay_secs, cycle_secs) для группы.
pub fn compute_group_timing(group_index: usize) -> (u64, u64) {
    let phase_offset = (group_index as u64) * PHASE_STEP_SECS;
    (phase_offset, CYCLE_SECS)
}

/// Проверяет что фазовый сдвиг последней группы не превышает цикл.
pub fn validate_schedule(num_groups: usize) -> bool {
    let max_phase = (num_groups.saturating_sub(1) as u64) * PHASE_STEP_SECS;
    if max_phase >= CYCLE_SECS {
        log::warn!(
            "Фазовый сдвиг последней группы ({}с) >= цикла ({}с). Увеличиваю цикл.",
            max_phase,
            CYCLE_SECS
        );
        // Не блокируем: просто предупреждение, работать всё равно будет
    }

    log::info!(
        "Расписание: {} групп, цикл={}с, сдвиг={}с",
        num_groups,
        CYCLE_SECS,
        PHASE_STEP_SECS
    );
    true
}